    private void buttonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonUpdateActionPerformed

        int p = JOptionPane.showConfirmDialog(null, "Are you sure you want to update?","Update Record",JOptionPane.YES_NO_OPTION);
        if(p==0){
                
         if(textStudentID.getText()==null || textFirstName.getText()==null || textLastName.getText()==null||  comboGender.getSelectedItem()==null
              || CmbLevel.getSelectedItem()==null      ){
                JOptionPane.showMessageDialog(null, "One or More Fields is empty");
            
            }else{

                try{

                    String sql= "update STUDENT set FIRSTNAME=?,LEVEL=?, LASTNAME=?,GENDER=? where STUDENTID=?";

                    ps=con.prepareStatement(sql);

                    ps.setString(1, textFirstName.getText());
                    ps.setString(2, CmbLevel.getSelectedItem().toString());
                    ps.setString(3, textLastName.getText());
                     ps.setString(4, comboGender.getSelectedItem().toString());
                   
                     ps.setString(5,textStudentID.getText().trim());
  

                    ps.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Record Updated");
                  clear();
                  showStudent();

                }catch(Exception e){
                    JOptionPane.showMessageDialog(null, e);
                    clear();
                }
            }
            
        }
        
    }//GEN-LAST:event_buttonUpdateActionPerformed

