    private void textStudentIDKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_textStudentIDKeyPressed
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            String sql = "select * from Student where StudentID='"+textStudentID.getText().trim()+"' " ;
            try{
                ps=con.prepareStatement(sql);
                rs=ps.executeQuery();
                if(rs.next()){
                    String ID=rs.getString("StudentID");
                    textStudentID.setText(ID);

                    String fName=rs.getString("FirstName");
                    textFirstName.setText(fName);

                    String level=rs.getString("LEVEL");
                    CmbLevel.setSelectedItem(level);

                    String lName=rs.getString("LastName");
                    textLastName.setText(lName);

                    String sex=rs.getString("GENDER");
                    comboGender.setSelectedItem(sex);

      

                }
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }
    }//GEN-LAST:event_textStudentIDKeyPressed

